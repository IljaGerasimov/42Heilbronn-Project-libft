#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	s_len;
	size_t	d_len;
	size_t	i;

	s_len = ft_strlen(src);
	d_len = 0;
	while ((d_len < size) && dst[d_len])
		d_len++;
	i = 0;
	if (d_len >= size)
		return (s_len + size);
	while (src[i] && (((d_len + i) < size - 1)))
	{
		dst[d_len + i] = src[i];
		i++;
	}
	dst[d_len + i] = '\0';
	return (d_len + s_len);
}
